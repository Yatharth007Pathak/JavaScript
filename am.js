// In JavaScript, conditional statements are used to perform different actions based on different conditions


// The if statement executes a block of code only if a specified condition is true.

let age = 20;

if (age >= 18) {
    console.log("You are eligible to vote.");
}

/*
Here's a breakdown of this conditional JavaScript code:

let age = 20;
Purpose: Declares a variable age with an initial value of 20.
Explanation: This sets the value of age as 20, which represents a person's age.

if (age >= 18) { ... }
Purpose: Uses an if statement to check if the value of age is 18 or higher.
Explanation: The condition (age >= 18) checks if age is greater than or equal to 18. Since age is 20, the condition evaluates to true.

console.log("You are eligible to vote.");
Purpose: Logs a message to the console if the condition is met.
Output: "You are eligible to vote."
Explanation: Since age is 20, the condition (age >= 18) is true, so this line executes, 
printing the message indicating the person is eligible to vote.
*/