/*
In JS, events are actions or occurrences that happen in the browser and can be detected and responded to using event handlers. 
Events can result from user interactions (like clicking a button, pressing a key, or resizing a window) 
or from browser activities (like loading a page or finishing an animation).
The change in the state of an object is known as an Event. 
Events are fired to notify code of "interesting changes" that may affect code execution.

Events are at the heart of interactive web development. Understanding how to attach event handlers, 
use the event object, and manage propagation is crucial for building dynamic and responsive applications.

Common Types of Events:

1. Mouse Events:
click: Triggered when an element is clicked.
dblclick: Triggered when an element is double-clicked.
mousedown: Triggered when the mouse button is pressed down.
mouseup: Triggered when the mouse button is released.
mousemove: Triggered when the mouse moves over an element.
mouseover: Triggered when the mouse enters an element.
mouseout: Triggered when the mouse leaves an element.

2. Keyboard Events:
keydown: Triggered when a key is pressed down.
keyup: Triggered when a key is released.
keypress: Triggered when a key is pressed (deprecated, use keydown).

3. Form Events:
submit: Triggered when a form is submitted.
change: Triggered when the value of a form element (like input or select) changes.
focus: Triggered when an element gains focus.
blur: Triggered when an element loses focus.

4. Window Events:
load: Triggered when the entire page (including images) has loaded.
resize: Triggered when the window is resized.
scroll: Triggered when the page is scrolled.
unload: Triggered when the page is about to be unloaded.

5. Touch Events (for mobile devices):
touchstart: Triggered when a touch point is placed on the screen.
touchmove: Triggered when a touch point is moved across the screen.
touchend: Triggered when a touch point is removed from the screen.

6. Other Events:
contextmenu: Triggered when the context menu (right-click) is opened.
animationstart, animationend, animationiteration: Triggered during CSS animations.
drag, dragstart, dragend: Triggered during drag-and-drop operations.
*/