// Special Features of Arrays

// Dynamic Typing: Arrays can hold elements of different types.
let mixed = [1, "apple", true, { name: "John" }];
console.log(mixed);

// Multi-dimensional Arrays: Arrays can contain other arrays (nested arrays).
let matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
];
console.log(matrix[1][2]); // Output: 6

// Length Property: The length property gives the number of elements in the array.
let fruits = ["apple", "banana"];
console.log(fruits.length); // 2