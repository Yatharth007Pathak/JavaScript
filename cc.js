/*
Arrays in JavaScript are used to store multiple values in a single variable. They are a versatile and powerful data structure 
that can hold elements of any type, including numbers, strings, objects, and even other arrays.
*/

// Creating array using Array Literals:
let heroes = ["hulk", "thor", "batman", "ironman"];
console.log(heroes);
console.log(heroes.length);

// Empty array:
let empty = [];
console.log(empty);

// Accessing Array Elements: Use the index (starting from 0) to access array elements.
let fruits = ["apple", "banana", "cherry"];
console.log(fruits[0]); // Output: "apple"
console.log(fruits[2]); // Output: "cherry"
console.log(fruits[3]); // Output: undefined